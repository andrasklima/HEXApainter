function draw_hexagons(finish_hexagons) {
  for (let index = 0; index < finish_hexagons.length; index++) {
    setTimeout(function () {
      let red_number = Math.floor(Math.random() * 255);
      let green_number = Math.floor(Math.random() * 255);
      let blue_number = Math.floor(Math.random() * 255);

      finish_hexagons[index].style.fill =
        "rgba(" + red_number + "," + green_number + "," + blue_number + ")";
    }, 20 * index);
  }
}

